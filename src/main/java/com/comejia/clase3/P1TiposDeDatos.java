package com.comejia.clase3;

public class P1TiposDeDatos {

    public static void main(String[] args) {

        int var1 = 40000000; // esto es un entero
        long var2 = 400000000000000L; // esto es un entero

        float var3 = 10.55555f; // esto es un flotante
        double var4 = 1000.33534547578; // esto es un flotante

        char var5 = 'p'; // esto es un char
        char var6 = '8'; // esto no es un numero, es un char

        boolean var7 = true; // esto es un booleano
        boolean var8 = false; // esto es un booleano

        System.out.println("Var1 = " + var1);
        System.out.println("Var2 = " + var2);
        System.out.println("Var3 = " + var3);
        System.out.println("Var4 = " + var4);
        System.out.println("Var5 = " + var5);
        System.out.println("Var6 = " + var6);
        System.out.println("Var7 = " + var7);
        System.out.println("Var8 = " + var8);

    }
}
