package com.comejia.clase2;

public class P2ImprimirDatos {

    public static void main(String[] args) {

        System.out.println(10); // esto un entero

        System.out.println(65.4789); // esto es un double o float (depende cual sea el default)

        System.out.println('p'); // esto es un char

        System.out.println('8'); // esto es un char
        System.out.println(8); // esto es un entero

        System.out.println(true); // esto es un booleano
        System.out.println(false); // esto es un booleano

        System.out.println("Hola, como estan?"); // esto es una cadena (String)
    }
}
